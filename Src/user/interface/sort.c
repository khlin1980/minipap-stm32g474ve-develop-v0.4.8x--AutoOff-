/**
 * @brief for qsort compare callback
 * 
 * @param a 
 * @param b 
 * @return int 
 */
int cmpfunc (const void *a, const void *b)
{
   uint32_t u32a = *(uint32_t*)a, u32b = *(uint32_t*)b;

   if(u32a > u32b)  return 1;

   else if(u32a < u32b)  return -1;
 
   return 0;
}


/**
 * @brief 
 * 
 * @param arr : pointer to array   
 * @param size : array size 
 */
void qsort_ascending(uint32_t arr[], uint32_t size)
{
  //* ascending order using qsort(stdlib)
  qsort((void*)arr, size, sizeof(uint32_t), cmpfunc);
}


